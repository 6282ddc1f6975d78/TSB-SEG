def select_best_grid_per_dataset(
    df_grid: pd.DataFrame,
    metric: str,
    higher_is_better: bool = True,
    min_coverage: float = 0.9,
) -> pd.DataFrame:
    """Select the best grid config per (algorithm, dataset).

    Selects ONE config per (algo, dataset) based on the mean score across
    all series of that dataset. Only configs whose series count is >=
    ``min_coverage`` × max_series (for that algo+dataset) are eligible for
    rank 1. Partial configs are ranked after all complete ones.

    Falls back to all configs if none meets the coverage threshold.
    """
    required = [metric, "algorithm", "dataset", "trial_index", "parent_run_id"]
    if not all(c in df_grid.columns for c in required):
        missing = [c for c in required if c not in df_grid.columns]
        print(f"Missing columns: {missing}")
        return pd.DataFrame()

    df = df_grid.dropna(subset=[metric, "trial_index"]).copy()

    # Per-config stats: mean score + series count
    config_stats = (
        df.groupby(["algorithm", "dataset", "parent_run_id"])
        .agg(config_mean=(metric, "mean"), n_series=("trial_index", "nunique"))
        .reset_index()
    )

    # Max series per (algo, dataset) — what a complete config has
    max_series = (
        config_stats.groupby(["algorithm", "dataset"])["n_series"]
        .max()
        .reset_index()
        .rename(columns={"n_series": "max_series"})
    )
    config_stats = config_stats.merge(max_series, on=["algorithm", "dataset"])
    config_stats["is_complete"] = config_stats["n_series"] >= (min_coverage * config_stats["max_series"])

    # Rank: complete configs first (by mean), then partial (by mean)
    # Sort key: (not is_complete ASC = complete first, then mean DESC)
    config_stats["_sort"] = config_stats["is_complete"].map({True: 0, False: 1})
    config_stats["config_rank"] = (
        config_stats
        .sort_values(["_sort", "config_mean"], ascending=[True, not higher_is_better])
        .groupby(["algorithm", "dataset"])
        .cumcount() + 1
    )
    # Correct: groupby + rank using a composite sort
    # More robust: use dense rank on (_sort, mean)
    config_stats = config_stats.sort_values(
        ["algorithm", "dataset", "_sort", "config_mean"],
        ascending=[True, True, True, not higher_is_better],
    )
    config_stats["config_rank"] = config_stats.groupby(["algorithm", "dataset"]).cumcount() + 1
    config_stats = config_stats.drop(columns=["_sort", "n_series", "max_series", "is_complete"])

    # Merge back to per-series rows
    df = df.merge(
        config_stats[["algorithm", "dataset", "parent_run_id", "config_rank", "config_mean"]],
        on=["algorithm", "dataset", "parent_run_id"],
    )

    return df


def aggregate_grid_summaries(
    df_grid_ranked: pd.DataFrame,
    metric: str,
) -> pd.DataFrame:
    """Compute best/worst/median per (algorithm, dataset) from ranked grid.

    Returns one row per (algorithm, dataset, trial_index) with columns:
    score_best, score_worst, score_median.

    When multiple configs share the same rank (ties), only one is kept
    per (algorithm, dataset, trial_index) to avoid inflating row counts.
    """
    dedup_cols = ["algorithm", "dataset", "trial_index"]

    # Best config: rank == 1 (break ties by keeping first parent_run_id)
    best = df_grid_ranked[df_grid_ranked["config_rank"] == 1].copy()
    best = best.drop_duplicates(subset=dedup_cols, keep="first")
    best = best.rename(columns={metric: "score_best"})

    # Worst config: max rank (break ties the same way)
    max_rank = df_grid_ranked.groupby(["algorithm", "dataset"])["config_rank"].max().reset_index()
    max_rank.columns = ["algorithm", "dataset", "max_rank"]
    worst_mask = df_grid_ranked.merge(max_rank, on=["algorithm", "dataset"])
    worst_mask = worst_mask[worst_mask["config_rank"] == worst_mask["max_rank"]]
    worst_agg = (
        worst_mask.groupby(dedup_cols)[metric]
        .first()
        .reset_index()
        .rename(columns={metric: "score_worst"})
    )

    # Median across configs
    median_agg = (
        df_grid_ranked.groupby(dedup_cols)[metric]
        .median()
        .reset_index()
        .rename(columns={metric: "score_median"})
    )

    # Merge all
    result = best[dedup_cols + ["score_best"]].copy()
    result = result.merge(worst_agg, on=dedup_cols, how="left")
    result = result.merge(median_agg, on=dedup_cols, how="left")

    return result


